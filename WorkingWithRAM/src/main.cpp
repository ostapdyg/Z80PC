void loop() 
{
  digitalWrite(CLK, LOW);
  delay(1);
  digitalWrite(CLK, HIGH);
  if(!digitalRead(WR_)){
    Serial.print("Write ");
  }
  else if(!digitalRead(RD_)){
    Serial.print("Read ");
  }
  sprintf(s, "Address : %04x Data: %x \n", ZPC_GetAddress(), ZPC_GetData());
  Serial.print(s);
  delay(1000);

}