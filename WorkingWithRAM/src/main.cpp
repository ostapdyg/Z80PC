void setup()
{

  char s[30];
  Serial.begin(9600);
  Serial.print("start\n");

  ZPC_ArduinoInit();
  
  uint16_t address = 0x00;
  for(uint16_t i=0; i< 80; i++){
    ZPC_MemWrite(address+i, program[i]);
  }

  for(uint16_t i=0; i< 80; i++){
    uint8_t read_data = ZPC_MemRead(address+i);
    sprintf(s, "%x ", read_data); //Should be equal to data
    Serial.print(s);
  }
  pinMode(CLK, OUTPUT);
  Serial.print("\n");

  ZPC_ProcStart();
}
char s[30];
